def ceasar(text_in, shift_amount, cipher_direction):
    display = ""
    if cipher_direction == "decode":
        shift_amount *= -1
    for letter in text_in:
        if letter in alphabet:
            position = alphabet.index(letter)
            new_posiiton = position + shift_amount
            new_letter = alphabet[new_posiiton]
            display += new_letter
        else:
            display += letter

    print(f"The {cipher_direction}d word is: {display}")


